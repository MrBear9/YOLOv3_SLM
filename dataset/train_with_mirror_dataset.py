import argparse
import json
import os
import shutil
import subprocess
import sys
from pathlib import Path

import yaml


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_SOURCE_YAML = ROOT / "data" / "military" / "data.yaml"
DEFAULT_SAFE_LABEL_ROOT = ROOT / "output" / "anchor_clustering" / "dataset_issue_scan" / "safe_remove" / "proposed_labels"
DEFAULT_MIRROR_ROOT = ROOT / "output" / "mirror_datasets" / "military_safe_remove_v1"
DEFAULT_TRAIN_SCRIPT = ROOT / "optical_teacher_yolo.py"
DEFAULT_TRAIN_OUTPUT = ROOT / "output" / "OpticalTeacherYOLO_mirror_safe_remove"


def windows_safe_path(path: Path) -> str:
    text = str(path)
    if os.name != "nt":
        return text
    if text.startswith("\\\\?\\"):
        return text
    try:
        resolved = path.resolve(strict=False)
    except Exception:
        resolved = Path(text)
    resolved_text = str(resolved)
    if resolved_text.startswith("\\\\?\\"):
        return resolved_text
    if resolved_text.startswith("\\\\"):
        return "\\\\?\\UNC\\" + resolved_text.lstrip("\\")
    return "\\\\?\\" + resolved_text


def path_exists_compat(path: Path) -> bool:
    return os.path.exists(windows_safe_path(path))


def remove_file_compat(path: Path):
    os.unlink(windows_safe_path(path))


def copy2_compat(src: Path, dst: Path):
    ensure_clean_parent(dst)
    shutil.copy2(windows_safe_path(src), windows_safe_path(dst))


def link_compat(src: Path, dst: Path):
    ensure_clean_parent(dst)
    os.link(windows_safe_path(src), windows_safe_path(dst))


def write_text_compat(path: Path, data: str, encoding: str = "utf-8"):
    ensure_clean_parent(path)
    with open(windows_safe_path(path), "w", encoding=encoding, newline="") as f:
        f.write(data)


def parse_args():
    parser = argparse.ArgumentParser(description="Build a trainable mirrored dataset without modifying the original dataset.")
    parser.add_argument("--source-yaml", type=Path, default=DEFAULT_SOURCE_YAML, help="Original dataset yaml path.")
    parser.add_argument("--safe-label-root", type=Path, default=DEFAULT_SAFE_LABEL_ROOT, help="Root directory of proposed labels generated by safe_remove.")
    parser.add_argument("--mirror-root", type=Path, default=DEFAULT_MIRROR_ROOT, help="Directory where the mirrored dataset will be created.")
    parser.add_argument("--train-script", type=Path, default=DEFAULT_TRAIN_SCRIPT, help="Training script to run after mirror preparation.")
    parser.add_argument("--train-output-dir", type=Path, default=DEFAULT_TRAIN_OUTPUT, help="Output directory for training on the mirrored dataset.")
    parser.add_argument("--teacher-init-mode", default=None, help="Optional override for OPTICAL_TEACHER_INIT_MODE.")
    parser.add_argument("--teacher-init-checkpoint", type=Path, default=None, help="Optional override for OPTICAL_TEACHER_INIT_CHECKPOINT.")
    parser.add_argument("--freeze-teacher", action="store_true", help="Optional override to freeze teacher during training.")
    parser.add_argument("--prepare-only", action="store_true", help="Only build the mirrored dataset and yaml, do not start training.")
    parser.add_argument("--force-rebuild", action="store_true", help="Rebuild mirror files even if they already exist.")
    parser.add_argument("--image-link-mode", choices=("hardlink", "copy"), default="hardlink", help="How to place images into the mirrored dataset.")
    return parser.parse_args()


def load_dataset_config(yaml_path: Path):
    with yaml_path.open("r", encoding="utf-8") as f:
        cfg = yaml.safe_load(f)

    names = cfg.get("names", [])
    if isinstance(names, dict):
        ordered_names = [names[idx] for idx in sorted(names)]
    else:
        ordered_names = list(names)

    raw_dataset_root = Path(cfg.get("path", "."))
    candidates = []
    if raw_dataset_root.is_absolute():
        candidates.append(raw_dataset_root)
    else:
        candidates.append((yaml_path.parent / raw_dataset_root).resolve())
        candidates.append((ROOT / raw_dataset_root).resolve())
        candidates.append(yaml_path.parent.resolve())
    dataset_root = next((candidate for candidate in candidates if candidate.exists()), candidates[0])

    splits = {}
    for split in ("train", "val", "test"):
        images_rel = cfg.get(split)
        if images_rel is None:
            continue
        split_path = Path(images_rel)
        images_dir = split_path if split_path.is_absolute() else (dataset_root / split_path).resolve()
        labels_dir = images_dir.parent / "labels"
        splits[split] = {
            "images_dir": images_dir,
            "labels_dir": labels_dir,
        }

    return cfg, dataset_root, ordered_names, splits


def ensure_clean_parent(path: Path):
    path.parent.mkdir(parents=True, exist_ok=True)


def place_image(src: Path, dst: Path, mode: str, force_rebuild: bool):
    ensure_clean_parent(dst)
    if path_exists_compat(dst):
        if not force_rebuild:
            return "skipped"
        remove_file_compat(dst)

    if mode == "hardlink":
        try:
            link_compat(src, dst)
            return "hardlinked"
        except OSError:
            copy2_compat(src, dst)
            return "copied_fallback"

    copy2_compat(src, dst)
    return "copied"


def place_label(src: Path, dst: Path, force_rebuild: bool):
    ensure_clean_parent(dst)
    if path_exists_compat(dst) and not force_rebuild:
        return "skipped"
    copy2_compat(src, dst)
    return "copied"


def build_mirror_dataset(args):
    cfg, dataset_root, class_names, splits = load_dataset_config(args.source_yaml.resolve())
    mirror_root = args.mirror_root.resolve()
    mirror_root.mkdir(parents=True, exist_ok=True)

    image_stats = {"hardlinked": 0, "copied": 0, "copied_fallback": 0, "skipped": 0}
    label_stats = {"proposed": 0, "original": 0, "copied": 0, "skipped": 0}

    for split_name, split_cfg in splits.items():
        source_images_dir = split_cfg["images_dir"]
        source_labels_dir = split_cfg["labels_dir"]
        proposed_labels_dir = args.safe_label_root.resolve() / split_name / "labels"

        mirror_images_dir = mirror_root / split_name / "images"
        mirror_labels_dir = mirror_root / split_name / "labels"
        mirror_images_dir.mkdir(parents=True, exist_ok=True)
        mirror_labels_dir.mkdir(parents=True, exist_ok=True)

        image_files = sorted([path for path in source_images_dir.iterdir() if path.is_file()])
        label_files = sorted([path for path in source_labels_dir.iterdir() if path.is_file() and path.suffix.lower() == ".txt"])

        for image_path in image_files:
            result = place_image(image_path, mirror_images_dir / image_path.name, args.image_link_mode, args.force_rebuild)
            image_stats[result] = image_stats.get(result, 0) + 1

        for label_path in label_files:
            proposed_label = proposed_labels_dir / label_path.name
            has_proposed_label = path_exists_compat(proposed_label)
            actual_label_source = proposed_label if has_proposed_label else label_path
            label_stats["proposed" if has_proposed_label else "original"] += 1
            result = place_label(actual_label_source, mirror_labels_dir / label_path.name, args.force_rebuild)
            label_stats[result] = label_stats.get(result, 0) + 1

    mirror_yaml = mirror_root / "data.yaml"
    mirror_cfg = {
        "path": str(mirror_root),
        "train": "train/images",
        "val": "val/images",
        "test": "test/images",
        "nc": cfg.get("nc", len(class_names)),
        "names": class_names,
    }
    write_text_compat(mirror_yaml, yaml.safe_dump(mirror_cfg, sort_keys=False, allow_unicode=True))

    metadata = {
        "source_yaml": str(args.source_yaml.resolve()),
        "source_dataset_root": str(dataset_root),
        "safe_label_root": str(args.safe_label_root.resolve()),
        "mirror_root": str(mirror_root),
        "mirror_yaml": str(mirror_yaml),
        "image_link_mode": args.image_link_mode,
        "force_rebuild": args.force_rebuild,
        "image_stats": image_stats,
        "label_stats": label_stats,
        "note": "Mirrored dataset built without modifying the original dataset. Images are hardlinked or copied; labels are copied into the mirror root.",
    }
    metadata_path = mirror_root / "mirror_build_summary.json"
    write_text_compat(metadata_path, json.dumps(metadata, indent=2, ensure_ascii=False))

    summary_txt = mirror_root / "mirror_build_summary.txt"
    write_text_compat(
        summary_txt,
        "\n".join([
            "Mirror Dataset Build Summary",
            f"Source yaml: {metadata['source_yaml']}",
            f"Source dataset root: {metadata['source_dataset_root']}",
            f"Safe label root: {metadata['safe_label_root']}",
            f"Mirror root: {metadata['mirror_root']}",
            f"Mirror yaml: {metadata['mirror_yaml']}",
            f"Image link mode: {metadata['image_link_mode']}",
            f"Image stats: {metadata['image_stats']}",
            f"Label stats: {metadata['label_stats']}",
            metadata["note"],
        ]),
    )

    return mirror_yaml, metadata_path


def run_training(args, mirror_yaml: Path):
    env = os.environ.copy()
    env["OPTICAL_TEACHER_YAML_PATH"] = str(mirror_yaml)
    env["OPTICAL_TEACHER_OUTPUT_DIR"] = str(args.train_output_dir.resolve())
    if args.teacher_init_mode:
        env["OPTICAL_TEACHER_INIT_MODE"] = str(args.teacher_init_mode)
    if args.teacher_init_checkpoint:
        env["OPTICAL_TEACHER_INIT_CHECKPOINT"] = str(args.teacher_init_checkpoint.resolve())
    if args.freeze_teacher:
        env["OPTICAL_TEACHER_FREEZE_TEACHER"] = "true"

    cmd = [sys.executable, str(args.train_script.resolve())]
    return subprocess.run(cmd, cwd=str(ROOT), env=env, check=False)


def main():
    args = parse_args()
    mirror_yaml, metadata_path = build_mirror_dataset(args)
    print(f"Mirror dataset yaml written to: {mirror_yaml}")
    print(f"Mirror dataset summary written to: {metadata_path}")

    if args.prepare_only:
        print("Prepare-only mode enabled; training was not started.")
        return

    result = run_training(args, mirror_yaml)
    if result.returncode != 0:
        raise SystemExit(result.returncode)


if __name__ == "__main__":
    main()
